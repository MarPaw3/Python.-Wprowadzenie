nudge = 1  # Podstawowe przypisanie
wink = 2
A, B = nudge, wink  # Przypisanie krotki
print(A, B)
[C, D] = [nudge, wink]  # Przypisanie listy
print(C, D)
(a, b, c) = [1, 2, 3]  # Przypisanie krotki watości do listy nazw
print(a, c)
(a, b, c) = 'ABC'  # Przypisanie łańcucha znaków dokrotki
print(a, c)