s = 'Mielonka'
print(len(s)) # drukuję długość stringa s
print(s[0]) # -"- pierwszy element sringa s
print(s[1]) # -"- drugi element stringa s
# pierwszy element w łańcuchu ma wartość 0
# string = łańcuch znaków
print(s[-1]) # wartości ujemne indeksują od końca łańcucha znaków
print(s[-2])
print(s[1:3]) # wycinek od 1 do 2 ( bez 3)
print(s[0:7]) # wycinek bez wartości granicznej lub
print(s[:-1]) # to samo inaczej wprowadzone
print(s[1:]) # wszystkie wartości od 1
print(s[:7]) # wszystkie wartości do 7
print(s[:]) # samo : drukuje wszystko